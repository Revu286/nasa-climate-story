        <article class="restoration-idea">
            <h2>Reforestation</h2>
            <img src="images/reforestation.jpeg" alt="Reforestation">
            <p>
                Reforestation is the process of planting trees in deforested or degraded lands to restore natural ecosystems, improve biodiversity, and combat climate change. By increasing tree cover, reforestation also helps in carbon sequestration.
            </p>
            <h3>Steps for Reforestation:</h3>
            <ol>
                <li>Identify suitable locations for reforestation based on ecological criteria.</li>
                <li>Select native tree species that are well-adapted to the region.</li>
                <li>Engage local communities in tree planting initiatives.</li>
                <li>Ensure long-term monitoring and maintenance of the reforested areas.</li>
            </ol>
        </article>

        <!-- Idea 2: Wetland Restoration -->
        <article class="restoration-idea">
            <h2>Wetland Restoration</h2>
            <img src="images/wetland_restoration.jpeg" alt="Wetland Restoration">
            <p>
                Wetlands are vital ecosystems that act as natural water filters and flood control systems. Restoring wetlands involves re-establishing hydrology, planting native vegetation, and removing pollutants to revive the ecosystem.
            </p>
            <h3>Steps for Wetland Restoration:</h3>
            <ol>
                <li>Remove any barriers that restrict natural water flow into the wetland.</li>
                <li>Plant native wetland species to restore plant diversity.</li>
                <li>Introduce wildlife back into the ecosystem to maintain ecological balance.</li>
                <li>Monitor water quality and biodiversity for long-term health of the wetland.</li>
            </ol>
        </article>

        <!-- Idea 3: Coral Reef Restoration -->
        <article class="restoration-idea">
            <h2>Coral Reef Restoration</h2>
            <img src="images/coral_reef_restoration.jpeg" alt="Coral Reef Restoration">
            <p>
                Coral reefs support marine biodiversity and protect coastlines from erosion. Restoration methods include transplanting corals, breeding resilient coral species, and creating artificial reefs to help damaged reefs recover.
            </p>
            <h3>Steps for Coral Reef Restoration:</h3>
            <ol>
                <li>Collect healthy coral fragments from existing reefs.</li>
                <li>Grow corals in nurseries to strengthen their resilience.</li>
                <li>Transplant grown corals to damaged reef areas.</li>
                <li>Use artificial reef structures to encourage marine biodiversity.</li>
            </ol>
        </article>

        <!-- Idea 4: Soil Regeneration -->
        <article class="restoration-idea">
            <h2>Soil Regeneration</h2>
            <img src="images/soil_regeneration.jpeg" alt="Soil Regeneration">
            <p>
                Healthy soil is critical for food production, carbon sequestration, and water retention. Soil regeneration focuses on restoring soil fertility through methods such as composting, crop rotation, and reducing chemical inputs.
            </p>
            <h3>Steps for Soil Regeneration:</h3>
            <ol>
                <li>Adopt no-till farming practices to prevent soil erosion.</li>
                <li>Incorporate organic matter like compost to improve soil health.</li>
                <li>Plant cover crops to protect soil and enhance biodiversity.</li>
                <li>Rotate crops to reduce nutrient depletion and prevent pests.</li>
            </ol>
        </article>
